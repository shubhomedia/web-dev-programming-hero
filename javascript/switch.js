
var color = 'red';

switch (color){
    case 'red':
        console.log("This is red block");
    break;
    case 'blue':
        console.log("This is blue block");
    break;
    case 'white':
        console.log("This is white block");
    break;
    case 'green':
        console.log("This is green block");
    break;
    case 'black':
        console.log("This is black block");
    break;
    default:
        console.log("This is No Colors Block");
}