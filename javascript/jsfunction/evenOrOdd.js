
// Even or Odd Number Function 

let number = 100;

function isEven (number) {
    if(number % 2 == 0){
        return true;
    }
    else {
        return false;
    }
}

isEven(12);

// Odd Number Function 

let number = 100;

function isOdd (number) {
    if(number % 2 != 0){
        return true;
    }
    else {
        return false;
    }
}

isOdd(12);